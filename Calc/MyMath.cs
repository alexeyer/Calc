using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calc
{
    class MyMath
    {
        public int Add(int a, int c)
        {
            return a + c;
        }

        public int Sub(int a, int b)
        {
            return a - b;
        }

        public int Multiple(int a, int b)
        {
            return a * b;
        }

        public int Divide(int a, int b)
        {
            return a / b;
        }
    }
}
